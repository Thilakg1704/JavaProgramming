package part1;

public class DataTypes {
	public static void main(String args[]) {
		
		// Data Types
		
		byte b = 127;    //8-bit integer
		short s = 32000; //16-bit integer
		int i = 100000;  //32-bit integer
		long l = 100000000000L; //64-bit integer
		
		
		float f = 10.5f; // 32-bit  floating point
		double d = 20.99; //64-bit floating point	
		
		char c = 'A'; //Character type
		boolean bool = true; // Boolean  type 
		
		
		System.out.println("Byte: "+b);
		System.out.println("Short: "+s);
		System.out.println("Integer: "+i);
		System.out.println("Long: "+l);
		System.out.println("Float: "+f);
		System.out.println("Double: "+d);
		System.out.println("char: "+c);
		System.out.println("Boolean: "+bool);
	}

}
